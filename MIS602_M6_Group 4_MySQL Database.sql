# Create the database schemas:

DROP DATABASE IF EXISTS mis602_ass3; 
CREATE SCHEMA mis602_ass3;
USE mis602_ass3;

# Create tables based on the logical model:

DROP TABLE IF EXISTS `Driver`;
CREATE TABLE Driver (
  driver_id VARCHAR(50) PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  phone VARCHAR(255) UNIQUE,
  dob DATE NOT NULL);
  
  DROP TABLE IF EXISTS `Truck`;
  CREATE TABLE Truck (
    truck_id  VARCHAR(50) PRIMARY KEY,
    availability VARCHAR(255) NOT NULL,
    capacity INT NOT NULL,
    type_of_truck VARCHAR(255) NOT NULL);

DROP TABLE IF EXISTS `Driver_schedule`;    
CREATE TABLE Driver_schedule (
    schedule_id VARCHAR(50) PRIMARY KEY,
    driver_id VARCHAR(50) NOT NULL,
    truck_id  VARCHAR(50) NOT NULL,
    FOREIGN KEY (driver_id) REFERENCES Driver(driver_id),
    FOREIGN KEY (truck_id) REFERENCES Truck(truck_id));

DROP TABLE IF EXISTS `Maintenance_schedule`; 
CREATE TABLE Maintenance_schedule (
    maintenance_schedule_id  VARCHAR(50),
    truck_id  VARCHAR(50) NOT NULL,
    maintenance_type VARCHAR(255) NOT NULL,
    date DATE NOT NULL,
    PRIMARY KEY (maintenance_schedule_id, truck_id), -- Composite primary key
    FOREIGN KEY (truck_id) REFERENCES Truck(truck_id));

DROP TABLE IF EXISTS `Bin`; 
CREATE TABLE Bin (
  bin_id VARCHAR(50) PRIMARY KEY,
  bin_size VARCHAR(255) NOT NULL,
  price DECIMAL(10,2) NOT NULL,
  location VARCHAR(255) NOT NULL,
  status VARCHAR(255) NOT NULL);

DROP TABLE IF EXISTS `Bin_order`; 
CREATE TABLE Bin_order (
  order_id VARCHAR(50) PRIMARY KEY,
  customer_id INT NOT NULL,
  bin_id VARCHAR(50) NOT NULL,
  day_requested DATE NOT NULL,
  street VARCHAR(255) NOT NULL,
  zip_code INT NOT NULL,
  quantity INT NOT NULL,
  total_amount DECIMAL(10,2) NOT NULL,
  payment_method VARCHAR(255) NOT NULL,
   CHECK (quantity > 0),
   CHECK (total_amount > 0));

DROP TABLE IF EXISTS `Bin_assignment`; 
CREATE TABLE Bin_assignment (
  bin_assignment_id VARCHAR(50) NOT NULL,
  bin_id VARCHAR(50) NOT NULL,
  bin_status VARCHAR(255) NOT NULL,
  order_id VARCHAR(50) NOT NULL,
  time_stamp TIMESTAMP NOT NULL,
  PRIMARY KEY (bin_assignment_id, bin_id,order_id),
  FOREIGN KEY (bin_id) REFERENCES Bin(bin_id),
  FOREIGN KEY (order_id) REFERENCES Bin_order(order_id));

DROP TABLE IF EXISTS `Bin_distribution`; 
CREATE TABLE Bin_distribution (
  distribution_id VARCHAR(50) NOT NULL,
  truck_id  VARCHAR(50) NOT NULL,
  order_id VARCHAR(50) NOT NULL,
  time_stamp TIMESTAMP NOT NULL,
  street VARCHAR(255) NOT NULL,
  zip_code INT NOT NULL,
  PRIMARY KEY (distribution_id, truck_id, order_id),
  FOREIGN KEY (truck_id) REFERENCES Truck(truck_id),
  FOREIGN KEY (order_id) REFERENCES Bin_order(order_id));

DROP TABLE IF EXISTS `Tracking_distribution`; 
CREATE TABLE Tracking_distribution (
  distribution_data_id VARCHAR(50) PRIMARY KEY,
  distribution_id VARCHAR(50) NOT NULL,
  start_location VARCHAR(255) NOT NULL,
  end_location VARCHAR(255) NOT NULL,
  estimated_time TIMESTAMP NOT NULL,
  time_stamp TIMESTAMP NOT NULL,
  FOREIGN KEY (distribution_id) REFERENCES Bin_distribution(distribution_id));

DROP TABLE IF EXISTS `Invoice`; 
CREATE TABLE Invoice (
  invoice_id VARCHAR(50) NOT NULL,
  order_id VARCHAR(50) NOT NULL,
  total_amount DECIMAL(10,2) NOT NULL,
  payment_method VARCHAR(255) NOT NULL,
  issued_date DATE NOT NULL,
  PRIMARY KEY (invoice_id, order_id),
  FOREIGN KEY (order_id) REFERENCES Bin_order(order_id),
  CHECK (total_amount > 0));

DROP TABLE IF EXISTS `Customer_type`; 
CREATE TABLE Customer_type (
  customer_type_id INT PRIMARY KEY,
  customer_type VARCHAR(255) NOT NULL);

DROP TABLE IF EXISTS `Customer`; 
CREATE TABLE Customer (
  customer_id INT PRIMARY KEY,
  customer_type_id INT NOT NULL,
  last_name VARCHAR(255) NOT NULL,
  first_name VARCHAR(255) NOT NULL,
  email VARCHAR(255) UNIQUE,
  phone VARCHAR(255) NOT NULL,
  street VARCHAR(255) NOT NULL,
  zip_code INT NOT NULL,
  FOREIGN KEY (customer_type_id) REFERENCES Customer_type(customer_type_id));

DROP TABLE IF EXISTS `Container`; 
CREATE TABLE Container (
  container_id VARCHAR(50) PRIMARY KEY,
  customer_id INT NOT NULL,
  capacity INT NOT NULL,
  FOREIGN KEY (customer_id) REFERENCES Customer(customer_id));

DROP TABLE IF EXISTS `Container_collection`; 
CREATE TABLE Container_collection (
  collection_id VARCHAR(50) NOT NULL,
  truck_id  VARCHAR(50) NOT NULL,
  container_id VARCHAR(50) NOT NULL,
  bin_assignment_id VARCHAR(50) NOT NULL,
  street VARCHAR(255) NOT NULL,
  zip_code INT NOT NULL,
  date_collected DATE NOT NULL,
  PRIMARY KEY (collection_id,truck_id,container_id,bin_assignment_id),
  FOREIGN KEY (container_id) REFERENCES Container(container_id),
  FOREIGN KEY (truck_id) REFERENCES Truck(truck_id),
  FOREIGN KEY (bin_assignment_id) REFERENCES Bin_assignment(bin_assignment_id));

DROP TABLE IF EXISTS `Notification`; 
CREATE TABLE Notification (
  notification_id VARCHAR(50) PRIMARY KEY,
  customer_id INT NOT NULL,
  content TEXT NOT NULL,
  time_stamp TIMESTAMP NOT NULL,
  FOREIGN KEY (customer_id) REFERENCES Customer(customer_id));

DROP TABLE IF EXISTS `Photo`; 
CREATE TABLE Photo (
  photo_id VARCHAR(50) PRIMARY KEY,
  customer_id INT NOT NULL,
  bin_assignment_id VARCHAR(50) NOT NULL,
  image_URL VARCHAR(255) NOT NULL,
  time_stamp TIMESTAMP NOT NULL,
  FOREIGN KEY (customer_id) REFERENCES Customer(customer_id),
  FOREIGN KEY (bin_assignment_id) REFERENCES Bin_assignment(bin_assignment_id));

DROP TABLE IF EXISTS `Tracking_collection`; 
CREATE TABLE Tracking_collection (
  collection_data_id VARCHAR(50) NOT NULL,
  collection_id VARCHAR(50) NOT NULL,
  start_location VARCHAR(255) NOT NULL,
  end_location VARCHAR(255) NOT NULL,
  estimated_time INT NOT NULL,
  time_stamp TIMESTAMP NOT NULL,
  PRIMARY KEY (collection_data_id, collection_id),
  FOREIGN KEY (collection_id) REFERENCES Container_collection(collection_id));

DROP TABLE IF EXISTS `Receipt_invoice`; 
CREATE TABLE Receipt_invoice (
  receipt_invoice_id VARCHAR(50) NOT NULL,
  invoice_id VARCHAR(50) NOT NULL,
  total_payment DECIMAL(10,2) NOT NULL,
  payment_method VARCHAR(255) NOT NULL,
  payment_date DATE NOT NULL,
  PRIMARY KEY (receipt_invoice_id, invoice_id),
  FOREIGN KEY (invoice_id) REFERENCES Invoice(invoice_id));

DROP TABLE IF EXISTS `Payment_method`; 
CREATE TABLE Payment_method (
  payment_method_id VARCHAR(50) PRIMARY KEY,
  customer_id INT NOT NULL,
  payment_method VARCHAR(255) NOT NULL,
  FOREIGN KEY (customer_id) REFERENCES Customer(customer_id));

DROP TABLE IF EXISTS `Collection_point`; 
CREATE TABLE Collection_point (
  collection_point_id VARCHAR(50) PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  address VARCHAR(255) NOT NULL,
  status VARCHAR(255) NOT NULL);

DROP TABLE IF EXISTS `Locate_collection_point`; 
CREATE TABLE Locate_collection_point (
  locate_id VARCHAR(50) PRIMARY KEY,
  customer_id INT NOT NULL,
  collection_point_id VARCHAR(50) NOT NULL,
  name VARCHAR(255) NOT NULL,
  address VARCHAR(255) NOT NULL,
  status VARCHAR(255) NOT NULL,
  map_URL VARCHAR(255) NOT NULL,
  FOREIGN KEY (customer_id) REFERENCES Customer(customer_id),
  FOREIGN KEY (collection_point_id) REFERENCES Collection_point(collection_point_id));

DROP TABLE IF EXISTS `Billing_payment`; 
CREATE TABLE Billing_payment (
  billing_payment_id VARCHAR(50) PRIMARY KEY,
  customer_id INT NOT NULL,
  total_payment DECIMAL(10,2) NOT NULL,
  payment_method VARCHAR(255) NOT NULL,
  issued_date DATE NOT NULL,
  FOREIGN KEY (customer_id) REFERENCES Customer(customer_id),
  CHECK (total_payment > 0));

DROP TABLE IF EXISTS `Receipt_payment`; 
CREATE TABLE Receipt_payment (
  receipt_payment_id  VARCHAR(50) NOT NULL,
  billing_payment_id VARCHAR(50) NOT NULL,
  total_payment DECIMAL(10,2) NOT NULL,
  payment_method VARCHAR(255) NOT NULL,
  payment_date DATE NOT NULL,
  PRIMARY KEY (receipt_payment_id, billing_payment_id),
  FOREIGN KEY (billing_payment_id) REFERENCES Billing_payment(billing_payment_id),
  CHECK (total_payment > 0));

DROP TABLE IF EXISTS `Achievement`;
CREATE TABLE Achievement (
  achievement_id  VARCHAR(50) NOT NULL,
  billing_payment_id VARCHAR(50) NOT NULL,
  green_point INT NOT NULL,
  PRIMARY KEY (achievement_id, billing_payment_id),
  FOREIGN KEY (billing_payment_id) REFERENCES Billing_payment(billing_payment_id));

DROP TABLE IF EXISTS `Charity`;
CREATE TABLE Charity (
  charity_id VARCHAR(50) PRIMARY KEY,
  charity_name VARCHAR(255) NOT NULL,
  address VARCHAR(255) NOT NULL,
  website VARCHAR(255) NOT NULL);

DROP TABLE IF EXISTS `Donation`;
CREATE TABLE Donation (
  donation_id VARCHAR(50) PRIMARY KEY,
  receipt_payment_id  VARCHAR(50) NOT NULL,
  charity_id VARCHAR(50) NOT NULL,
  percentage DECIMAL(5,2) NOT NULL,
  donation_amount DECIMAL(10,2) NOT NULL,
  donation_tax_receipt VARCHAR(3) NOT NULL,
  FOREIGN KEY (receipt_payment_id) REFERENCES Receipt_payment(receipt_payment_id),
  FOREIGN KEY (charity_id) REFERENCES Charity(charity_id),
  CHECK (donation_amount > 0));

DROP TABLE IF EXISTS `Fine`;
CREATE TABLE Fine (
  fine_id VARCHAR(50) PRIMARY KEY,
  receipt_payment_id  VARCHAR(50) NOT NULL,
  amount DECIMAL(10,2) NOT NULL,
  payment_method VARCHAR(255) NOT NULL,
  payment_date DATE NOT NULL,
  fine_type VARCHAR(255) NOT NULL,
  FOREIGN KEY (receipt_payment_id) REFERENCES Receipt_payment(receipt_payment_id),
  CHECK (amount > 0));

DROP TABLE IF EXISTS `Wasted_item`;
CREATE TABLE Wasted_item (
  item_id VARCHAR(50) PRIMARY KEY,
  item_name VARCHAR(255) NOT NULL,
  category VARCHAR(255) NOT NULL,
  capacity INT NOT NULL,
  CHECK (capacity > 0));

DROP TABLE IF EXISTS `Checking_process`;
CREATE TABLE Checking_process (
  checking_id VARCHAR(50) PRIMARY KEY,
  item_id VARCHAR(50) NOT NULL,
  container_id VARCHAR(50) NOT NULL,
  billing_payment_id VARCHAR(50) NOT NULL,
  number_item INT NOT NULL,
  category VARCHAR(255) NOT NULL,
  time_stamp TIMESTAMP NOT NULL,
  FOREIGN KEY (item_id) REFERENCES Wasted_item(item_id),
  FOREIGN KEY (container_id) REFERENCES Container(container_id),
  FOREIGN KEY (billing_payment_id) REFERENCES Billing_payment(billing_payment_id),
  CHECK(number_item > 0));

