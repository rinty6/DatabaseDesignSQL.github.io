# 4 queries to retrieve information from the database

#4.1 Find customer types with an average donation percentage greater than 20%.
SELECT 
    ct.customer_type, 
    ROUND(AVG(d.percentage), 2) AS avg_donation_percentage
FROM 
    Customer c
    JOIN Customer_type ct ON c.customer_type_id = ct.customer_type_id
    JOIN Billing_payment bp ON c.customer_id = bp.customer_id
    JOIN Receipt_payment rp ON bp.billing_payment_id = rp.billing_payment_id
    JOIN Donation d ON rp.receipt_payment_id = d.receipt_payment_id
GROUP BY ct.customer_type
HAVING AVG(d.percentage) > 20;

#4.2 Find the 5 collection points customers have located the most, along with their addresses.
SELECT cp.name, cp.address, visit_count
FROM (
  SELECT collection_point_id, COUNT(locate_id) AS visit_count
  FROM Locate_collection_point
  GROUP BY collection_point_id
) AS visit_counts
JOIN Collection_point cp ON visit_counts.collection_point_id = cp.collection_point_id
ORDER BY visit_count DESC
LIMIT 5;

#4.3 Retrieve each customer type's total number of items recycled and the corresponding environmental impact (water, energy, landfill, and carbon emissions saved).
SELECT
    ct.customer_type, subquery.total_items, subquery.water_saved, subquery.energy_saved,
    subquery.landfill_saved, subquery.carbon_saved
FROM Customer_type ct
    JOIN (
        SELECT c.customer_type_id, SUM(cp.number_item) AS total_items,
            SUM(cp.number_item * 5.87) AS water_saved,
            SUM(cp.number_item * 1.30) AS energy_saved,
            SUM(cp.number_item * 0.06) AS landfill_saved,
            SUM(cp.number_item * 0.12) AS carbon_saved
        FROM
            Checking_process cp
            JOIN Billing_payment bp ON cp.billing_payment_id = bp.billing_payment_id
            JOIN Customer c ON bp.customer_id = c.customer_id
        GROUP BY
            c.customer_type_id
    ) AS subquery ON ct.customer_type_id = subquery.customer_type_id;
    
#4.4 Find the customers who have a total of over 300 green points.
SELECT c.first_name, c.last_name, SUM(a.green_point) 
AS total_green_points
FROM Customer c
JOIN Billing_payment bp ON c.customer_id=bp.customer_id
JOIN Achievement a ON bp.billing_payment_id = a.billing_payment_id
GROUP BY c.customer_id
HAVING total_green_points >=300;








